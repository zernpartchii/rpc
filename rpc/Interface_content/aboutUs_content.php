                    <h4 class="mb-12">Research Agenda</h4>
                    <p align="justify">The University Research Program is guided by “The Sustainable Development Agenda Framework” that encompasses the following areas:</p>
                    <p align="justify">Sustainable economy area focuses on research related to eco-friendly enterprise development and business practices.</p>
                    <p align="justify">Sustainable industries and technology area focuses mainly on research that seeks to invent, innovate, improve, and enhance the industrial utilization and technology application that are recognized as “green” technologies. These center on the relevance of these technologies in the local setting.</p>
                    <p align="justify">Sustainable Environment area focuses on the sound and balanced interaction with ecology. Researches are on climate change, disaster mitigation, renewable energy, and the preservation of the environment.</p>
                    <p align="justify">Sustainable Institutions area seeks to integrate the human and social components in sustainable development. Researches concentrate on leadership education, rule of law, good governance, public health, welfare and culture.</p>
                    <p align="justify">To be able to adequately respond to the challenges and opportunities brought about by the changing social demands, the University recognizes the need to continuously strengthen its systems and sustain its institutional effectiveness and efficiency. Hence, to ensure the relevance of its programs, mechanisms and services, UM also conducts researches for their continual improvement.</p>
