                        <h4 class="mb-12">About Us</h4>
                        <p align="justify">The University of Mindanao acknowledges the vital role of research as a catalyst in the promotion of instruction and extension in higher education. As such, it focuses its strategic direction on the development of a strong research culture that would enable its faculty, staff and students to be engaged in research that is responsive to the local, regional and national development needs.</p>
                        <p align="justify">The Research and Publication Center (RPC) was established to manage and implement the research program of the University. It specifically identifies commitment to: operate on a relevant research agenda; develop skilled, committed, conscientious, and prudent researchers; upgrade research-support mechanisms such as facilities, devices, policies and systems; produce, present, publish and practice researches that respond to the needs of the time and contribute to the sustainable development of society; and establish and sustain strategic linkages with external agencies for purposes of collaboration and funding.</p>
