                    <h4 class="mb-12">Funding Opportunities</h4>
                    <p align="justify">Internal funding for research projects may be accessed through the following:</p>
                    <ul class="list-styled">
                        <li>Institutional Research Grant given to faculty and staff whose research proposals are in consonance with the research agenda and pass the criteria stipulated in OPM 14.01.</li>
                        <li>Dolores P. Torres Research Grant is a competitive grant which aims to provide financial support to outstanding graduate and undergraduate student researchers.</li>
                        <li>Guillermo E. Torres Research Fellowship is open to all researchers from other institutions. Candidates must have a relevant master’s degree in minimum and be an independent scholar with a strong research achievement.</li>
                    </ul>
                    <p align="justify">External funding for research projects are accessed through RPC’s external partners/collaborators.</p>
                    <p align="justify">Financial support is given to faculty and staff for paper presentations and publications subject to institutional policies, standards and requirements.</p>
